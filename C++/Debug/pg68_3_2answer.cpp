#include <iostream>
using namespace std;
const double pi = 3.14; 
double squareArea(double r)
{
    return r = r * r;
}
double circleArea(double r)
{
    return r = pi * r * r;
}
int vardaanmain()
{
    double a = 10.7;
    cout << squareArea(a) << " " << endl;
    cout << circleArea(a) << " " << endl;
    cout << a << endl;
    return 0;
}
